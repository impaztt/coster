// Global tuning knob: raises all achievement ticket rewards while preserving
// relative progression tiers across the catalog.
const _achievementTicketScale = 1.35;

int _scaledTicketReward(int base) {
  final scaled = (base * _achievementTicketScale).round();
  // Keep progression feeling better at every tier by guaranteeing +1 minimum.
  return scaled > base ? scaled : (base + 1);
}

/// Convenience: numeric target achievements.
AchievementDef _num({
  required String id,
  required String name,
  required String description,
  required AchievementCategory category,
  required int ticketReward,
  required double Function(AchContext) current,
  required double target,
}) {
  return AchievementDef(
    id: id,
    name: name,
    description: description,
    category: category,
    ticketReward: _scaledTicketReward(ticketReward),
    progress: (ctx) => AchProgress(current(ctx), target),
  );
}

/// Boolean achievements (target = 1).
AchievementDef _bool({
  required String id,
  required String name,
  required String description,
  required AchievementCategory category,
  required int ticketReward,
  required bool Function(AchContext) test,
}) {
  return AchievementDef(
    id: id,
    name: name,
    description: description,
    category: category,
    ticketReward: _scaledTicketReward(ticketReward),
    progress: (ctx) => AchProgress(test(ctx) ? 1 : 0, 1),
  );
}

